#include <stdio.h>

int main() {
    // Printing ASCII values for specific character constants
    printf("ASCII value for '\\n' (Newline): %d\n", '\n');
    printf("ASCII value for '\\r' (Carriage Return): %d\n", '\r');
    printf("ASCII value for '\\t' (Horizontal Tab): %d\n", '\t');
    printf("ASCII value for '\\a' (Alert/Bell): %d\n", '\a');
    printf("ASCII value for '\\b' (Backspace): %d\n", '\b');

    return 0;
}
